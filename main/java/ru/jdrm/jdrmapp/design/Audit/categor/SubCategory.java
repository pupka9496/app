package ru.jdrm.jdrmapp.design.Audit.categor;

public class SubCategory {
    private String evaluation;
    private String requirements_of_the_roadmap;
    private String comments;

    public SubCategory(){}

    public String getEvaluation() {
        return evaluation;
    }

    public void setEvaluation(String evaluation) {
        this.evaluation = evaluation;
    }

    public String getRequirements_of_the_roadmap() {
        return requirements_of_the_roadmap;
    }

    public void setRequirements_of_the_roadmap(String requirements_of_the_roadmap) {
        this.requirements_of_the_roadmap = requirements_of_the_roadmap;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

}